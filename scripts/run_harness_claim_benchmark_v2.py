"""Run the Claim Benchmark v2 for Adaptive Skill System.

Expanded from v1 (18 cases) to v2 (36 cases).
Key additions vs v1 runner:
- Imports from ``claim_benchmark_v2_suite``
- Reports difficulty-tier slice metrics (easy/medium/hard)
- Default baseline-id is ``claim-benchmark-v2``

Usage examples::

    # Run the suite and inspect results
    python scripts/run_harness_claim_benchmark_v2.py

    # Lock a new baseline
    python scripts/run_harness_claim_benchmark_v2.py \\
        --baseline-out harness_baselines/claim-benchmark-v2.json

    # Compare against an existing baseline and fail on regression
    python scripts/run_harness_claim_benchmark_v2.py \\
        --baseline harness_baselines/claim-benchmark-v2.json \\
        --fail-on-regression
"""

from __future__ import annotations

import argparse
import json
import sys
from datetime import datetime, timezone
from math import sqrt
from pathlib import Path

PROJECT_ROOT = Path(__file__).resolve().parents[1]
if str(PROJECT_ROOT) not in sys.path:
    sys.path.insert(0, str(PROJECT_ROOT))

from adaptive_skill import __version__
from adaptive_skill.harness.baseline import BaselineRecord
from adaptive_skill.harness.claim_benchmark_v2_suite import (
    CLAIM_BENCHMARK_V2_SUITE_ID,
    DEFAULT_BATCH_ID_V2,
    run_claim_benchmark_v2,
)
from adaptive_skill.harness.cli import load_baseline_record
from adaptive_skill.harness.metrics import compute_metrics
from adaptive_skill.harness.regression import RegressionThresholds, check_regression
from adaptive_skill.harness.reporting import write_report_bundle

DEFAULT_OUTPUT_DIR = ".benchmark-artifacts/claim-benchmark-v2"
DEFAULT_BATCH_FILE = "claim-benchmark-v2-batch-result.json"
DEFAULT_METRICS_FILE = "claim-benchmark-v2-metrics.json"
DEFAULT_FILE_STEM = "adaptive-skill-harness-claim-benchmark-v2"
DEFAULT_REPORT_TITLE = "Adaptive Skill Harness Claim Benchmark v2"
DEFAULT_BASELINE_ID = "claim-benchmark-v2"
DEFAULT_BASELINE_LABEL = "claim-benchmark-v2"
DEFAULT_P95_LATENCY_INCREASE_PCT = 200.0


def build_parser() -> argparse.ArgumentParser:
    parser = argparse.ArgumentParser(
        description=(
            "运行 claim-benchmark-v2（36-case seeded suite，难度分层），"
            "生成 BatchResult / Metrics / Report，并可锁 baseline。"
        )
    )
    parser.add_argument("--output-dir", default=DEFAULT_OUTPUT_DIR)
    parser.add_argument("--batch-file", default=DEFAULT_BATCH_FILE)
    parser.add_argument("--metrics-file", default=DEFAULT_METRICS_FILE)
    parser.add_argument("--file-stem", default=DEFAULT_FILE_STEM)
    parser.add_argument("--title", default=DEFAULT_REPORT_TITLE)
    parser.add_argument("--system-version", default=__version__)
    parser.add_argument("--batch-id", default=DEFAULT_BATCH_ID_V2)
    parser.add_argument("--baseline-out", help="锁定 baseline JSON 到指定路径")
    parser.add_argument("--baseline-id", default=DEFAULT_BASELINE_ID)
    parser.add_argument("--baseline-label", default=DEFAULT_BASELINE_LABEL)
    parser.add_argument("--baseline", help="已有 baseline JSON 路径，提供后执行 regression check")
    parser.add_argument("--fail-on-regression", action="store_true")

    thresholds = parser.add_argument_group("Regression thresholds")
    thresholds.add_argument("--pass-rate-drop", type=float, default=0.05)
    thresholds.add_argument("--avg-score-drop", type=float, default=0.05)
    thresholds.add_argument("--hard-fail-increase", type=int, default=0)
    thresholds.add_argument("--error-rate-increase", type=float, default=0.05)
    thresholds.add_argument(
        "--p95-latency-increase-pct", type=float,
        default=DEFAULT_P95_LATENCY_INCREASE_PCT,
    )
    thresholds.add_argument("--case-score-drop", type=float, default=0.10)
    return parser


def _write_json(path: Path, payload: object) -> None:
    path.parent.mkdir(parents=True, exist_ok=True)
    path.write_text(json.dumps(payload, ensure_ascii=False, indent=2), encoding="utf-8")


def _build_thresholds(args: argparse.Namespace) -> RegressionThresholds:
    return RegressionThresholds(
        pass_rate_drop=args.pass_rate_drop,
        avg_score_drop=args.avg_score_drop,
        hard_fail_increase=args.hard_fail_increase,
        error_rate_increase=args.error_rate_increase,
        p95_latency_increase_pct=args.p95_latency_increase_pct,
        case_score_drop=args.case_score_drop,
    )


def _build_baseline(metrics, *, baseline_id, label, system_version) -> BaselineRecord:
    return BaselineRecord(
        baseline_id=baseline_id,
        label=label,
        system_version=system_version,
        locked_at=datetime.now(timezone.utc).isoformat(),
        metrics=metrics,
        notes=(
            "Locked from claim-benchmark-v2 (36-case, difficulty-stratified seeded suite). "
            "Generated by scripts/run_harness_claim_benchmark_v2.py. "
            "This baseline is the v2 release-grade evidence source."
        ),
        metadata={
            "suite": CLAIM_BENCHMARK_V2_SUITE_ID,
            "generated_by": "scripts/run_harness_claim_benchmark_v2.py",
            "seed_mode": "in-memory-kb-ltm",
            "n_cases": 36,
            "layer_split": "L1:12 / L2:12 / L3:12",
            "difficulty_split": "easy:4 / medium:4 / hard:4 per layer",
        },
    )


def _wilson_lower(k: int, n: int, z: float = 1.96) -> float:
    if n == 0:
        return 0.0
    p = k / n
    denom = 1 + z * z / n
    centre = (p + z * z / (2 * n)) / denom
    half = z * sqrt((p * (1 - p) + z * z / (4 * n)) / n) / denom
    return max(0.0, centre - half)


def _difficulty_slice_summary(batch) -> str:
    """Return a summary string breaking down pass rates by difficulty tier."""
    tiers = ["easy", "medium", "hard"]
    lines = []
    for tier in tiers:
        tier_results = [
            r for r in batch.results
            if r.case_id and tier in r.case_id
        ]
        if not tier_results:
            continue
        n = len(tier_results)
        k = sum(1 for r in tier_results if r.final_status == "pass")
        lines.append(f"  {tier:6s}: {k}/{n} = {k/n:.1%}")
    return "\n".join(lines) if lines else "  (difficulty data unavailable)"


def main(argv: list[str] | None = None) -> int:
    parser = build_parser()
    args = parser.parse_args(argv)

    output_dir = Path(args.output_dir)
    batch_path = output_dir / args.batch_file
    metrics_path = output_dir / args.metrics_file
    reports_dir = output_dir / "reports"

    batch = run_claim_benchmark_v2(
        system_version=args.system_version,
        batch_id=args.batch_id,
    )
    metrics = compute_metrics(batch)

    _write_json(batch_path, batch.to_dict())
    _write_json(metrics_path, metrics.to_dict(rounded=False))

    if args.baseline_out:
        rec = _build_baseline(
            metrics,
            baseline_id=args.baseline_id,
            label=args.baseline_label,
            system_version=args.system_version,
        )
        _write_json(Path(args.baseline_out), rec.to_dict())
        print(f"  Baseline locked: {args.baseline_out}")

    loaded_baseline = load_baseline_record(args.baseline) if args.baseline else None
    regression_report = None
    if loaded_baseline is not None:
        regression_report = check_regression(
            metrics, loaded_baseline, thresholds=_build_thresholds(args)
        )

    written = write_report_bundle(
        str(reports_dir),
        batch,
        metrics=metrics,
        baseline=loaded_baseline,
        regression_report=regression_report,
        title=args.title,
        file_stem=args.file_stem,
    )

    n_total = batch.summary.total
    n_passed = batch.summary.passed
    wilson_lower = _wilson_lower(n_passed, n_total)

    print("Claim Benchmark v2 completed:")
    print(f"  BatchResult  : {batch_path}")
    print(f"  Metrics      : {metrics_path}")
    print(f"  Report JSON  : {written.json_path}")
    print(f"  Report MD    : {written.markdown_path}")
    print(f"  Report HTML  : {written.html_path}")
    print(
        f"  Summary      : total={n_total}, passed={n_passed}, "
        f"pass_rate={n_passed/n_total:.1%}"
    )
    print(
        f"  Wilson 95% CI lower bound: {wilson_lower:.4f}  "
        f"(N={n_total}, k={n_passed})"
    )
    print("  Difficulty breakdown:")
    print(_difficulty_slice_summary(batch))

    if regression_report is not None:
        verdict = getattr(regression_report, "verdict", "UNKNOWN")
        print(f"  Regression   : {verdict}")
        if args.fail_on_regression and verdict == "FAIL":
            return 2

    return 0


if __name__ == "__main__":
    sys.exit(main())
